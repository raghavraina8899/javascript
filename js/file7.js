// trim

let firstName = "   Raghav    ";

console.log(firstName.length);
firstName = firstName.trim();
console.log(firstName.length);

// toUpperCase()

console.log(firstName);
firstName = firstName.toUpperCase();
console.log(firstName);

// toLowerCase()

console.log(firstName);
firstName = firstName.toLowerCase();
console.log(firstName);

// slice

console.log(firstName);
firstName = firstName.slice(0,4);
console.log(firstName);
